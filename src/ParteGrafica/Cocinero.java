    private void OrdenListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OrdenListaMouseClicked
       
        if (ListaPedido.getText().equals("Usted no tiene ordenes asignadas")) {
            JOptionPane.showMessageDialog(null,"No hay ninguna orden que cocinar");
        } else {
            cocinar();
            if (RestauranteLaProsperidad.ptr == null) {
                System.out.println("Lista vacia");
            } else {
                Orden t = new Orden();
                t = RestauranteLaProsperidad.ptr;

                while (t != null) {

                    String[] info = (t.plato).split(";");

                    if ((Cocinero.id).equals(info[4])) {
                        t.plato = info[0] + ";" + info[1] + ";" + info[2] + ";L;" + info[4];
                    }

                    t = t.link;

                }
            }
            this.setEnabled(true);
            
        }
    }//GEN-LAST:event_OrdenListaMouseClicked

