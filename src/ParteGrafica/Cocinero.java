    public Cocinero() {
        initComponents();
        if (RestauranteLaProsperidad.ptr == null) {
            System.out.println("Lista vacia");
        } else {
            Orden t = new Orden();
            t = RestauranteLaProsperidad.ptr;
            int c = 0;
            while (t != null && c == 0) {

                String[] info = (t.plato).split(";");
                if ((Cocinero.id).equals(info[4])) {
                    c = 1;
                    NumOrden.setText("Orden #" + info[0]);
                    if (ListaPedido.getText().equals("")) {
                        ListaPedido.setText(info[2]);
                    } else {
                        ListaPedido.setText(ListaPedido.getText() + "\n" + info[2]);
                    }
                    
                } else {
                    ListaPedido.setText("Usted no tiene ordenes asignadas");
                }

                t = t.link;

            }
        }
    }

